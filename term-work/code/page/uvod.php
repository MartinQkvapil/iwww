            <article class="slovoUvodem">Koncem 19. století dochází ke zvýšenému rozvoji hospodářské činnosti, stoupá české národní sebevědomí a dochází k aktivizaci spolkové činnosti. V obcích se dostávají ke slovu nové myšlenky, zakládají se hasičské sbory, Sokol, různé osvětové spolky, ale i nové politické strany a hospodářská sdružení občanů za účelem zvýšení prosperity jejich hospodářství. K těmto spolkům patří také družstevní mlékárny a spořitelní a záložní spolky.
                Vzhledem k blízkosti obcí Orlice a Kunčice a vzájemné znalosti hospodářů z obou obcí došlo v roce 1901 k založení společné družstevní mlékárny na Orlici a 26.1.1902 na společné schůzi hospodářů z obou obcí k ustanovení "Spořitelního záložního spolku pro Kunčici a Orlici společenstva s ručením neomezeným v Kunčicích". Mezi hlavními iniciátory vzniku této finanční instituce jsou jmenováni Jan Reinelt (1847-1911), továrník a kunčický starosta, Alois Kněžek (1861-1948), řídící na Kunčicích a P. Adolf Hubálek (1871-1959), tč. kyšperský kaplan. Smyslem činnosti spolku, který později převzal do názvu pojmenování KAMPELIČKA (na počest myšlenek F. C. Kampelíka), byla vzájemná pomoc jeho členů při poskytování úvěrů, za podmínek u jiných spolků nedosažitelných. Zárukou byla znalost všech členů i funkcionářů navzájem, poněvadž činnost spolku se omezovala pouze na malý region.
                Přísné předpisy pro hospodaření těchto spolků zakazovaly spekulace a podnikání s prostředky spoluobčanů a limitovaly úrokovou míru půjček. Velkou pomocí pro členy byl i společný a tedy lacinější nákup některých hospodářských potřeb jako hnojiva, osiva, krmiva, stroje apod. Že bylo hospodaření vedeno vesměs obezřetně je patrné ze skutečnosti, že během celého období první republiky neutrpěly kampeličky prakticky žádné ztráty. Činnost kampeliček byla pomocí hlavně pro malé a střední hospodáře, kteří nebyli schopni vydat v případě potřeby větší finanční prostředky na investice nebo při pomoci v nouzi a upadali tak do rukou lichvářů a bezohledných institucí. Činnost kampeličky ve smyslu záložního družstva, trvala do roku 1950, potom sloužila budova jako úřadovna České spořitelny v Letohradě. Činnost tedy ustala a jako mnoho jiných věcí chátrala i budova Kampeličky, ve které byla zřízena klubovna požárního sboru Kunčice, klubovna SSM a pobočka městské knihovny.</article>
